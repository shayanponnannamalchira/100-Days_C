//For Loop
#include <stdio.h>

int main()
{
    int i;
    for(i=1; i<=10; i++)
    {
        printf("%d", i);
    }
}

//While Loop
int main() {
    int a = 1;
    while (a <= 10) {
        printf("%d\n", a);
        a++;
    }
    return 0;
}

//Do While Loop
int main() {
    int a = 1;
    do {
        printf("%d\n", a);
        a++;
    } while (a <= 10);
    return 0;
}
